
# Comparison operators

number_1 = 1
number_2 = 2

# == return true if the value of number_1 is equal to number_2

print(f'number_1 == number_2 :{number_1==number_2}')

# != return true if the value of number_1 is not equal to number_2
print(f'{number_1} != {number_2} :{number_1!=number_2}')

# > : return true if the value of number_1 greater than number_2
print(f'{number_1} > {number_2} :{number_1>number_2}')

# >= : return true if the value of number_1 equal or greater than number_2
print(f'{number_1} >= {number_2} :{number_1 >= number_2}')

# < : return true if the value of number_1 less than number_2
print(f'{number_1} < {number_2} :{number_1 < number_2}')

# <= : return true if the value of number_1 equal or Less than number_2
print(f'{number_1} <= {number_2} :{number_1 <= number_2}')


